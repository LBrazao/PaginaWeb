	  <p style="font-family:Oswald; font-size:17px; font-weight:lighter ">José Luis Barraza, hoy director de TC… La Revista en sus distintas versiones (gráfica, radial y digital) comenzó su relación con el automovilismo cuando trabajando en una empresa electrónica se pone de acuerdo con el directorio de dicha empresa y organiza una gira por la Mesopotamia promocionando los productos que ellos fabricaban. Partió desde Paraná curzando toda la costa del Río Uruguay hasta Misiones y de ahí a San Pablo, Brasil para ver correr a Carlos Reutemann. José Luis Barraza, de común acuerdo con la empresa, hace primero el trabajo correspondiente a la misma y luego lo autorizan a cruzar “el charco”.<br />
<br />
Un periodista Jorge Drago se entera del viaje y le pregunta si es verdad que va a ver correr a Reutemann, en este caso Drago le comenta que tendría que mandar a dos periodistas, y si Barraza lo podría llevar en su auto, a lo que este acepta sin inconvenientes, quedando de acuerdo en compartir los gastos. Los periodistas en cuestión son Héctor Navone y Marín.
<br />
<br />Hacen todo el recorrido hasta llegar a Iguazú para llegar al lado brasileño, en ese momento no había puente para cruzar, había que hacerlo por barco y el río estaba crecido habiendo tapado un muelle, impidiendo la embarcación de las tres personas. Es así que retroceden hasta la localidad de Irigoyen por frontera seca y cruzan por un lugar muy complicado de difícil acceso. Esto hace que se retrasen rompiendo inclusive los neumáticos. Ellos tenían programado llegar el jueves por la noche o el viernes por la mañana y recién lo estaban haciendo el sábado por la mañana.
<br />
<br />Navone y Marín tenían que transmitir la carrera de Fórmula 1 por Radio Antártida pero el tiempo no los favorecía estando complicados para el retiro de credenciales y, obviamente, el pedido de líneas para transmitir. Héctor Navone, responsable de la transmisión en San Pablo, sufre una crisis de nervios de tal forma que quería regresar a Buenos Aires por no llegar a tiempo a los trámites previos a la transmisión. A todo esto Marín, el otro periodista, le pide a Barraza que haga algo, que impida el aborto de la transmisión, ya que en Buenos Aires estaban todos a la espera de que esta se de. Barraza estaciona el auto. Sin saber nada de las gestiones asume la responsabilidad y consigue las credenciales, la línea para transmisión, el lugar donde se debía realizar la transmisión y todo salió perfecto.
<br />
<br />En el regreso de Brasil a Buenos Aires, por los nervios y la tensión Héctor Navone durmió todo el viaje y Barraza se reintegra a su actividad normal con la empresa donde estaba empleado.
En ningún momento José Luis Barraza comentó su gestión en Brasil por respeto a Héctor Navone y para no perjudicarlo ante su socio Drago.
<br />
<br />En un momento dado Jorge Drago, que se había enterado por Marín lo sucedido en Brasil, le ofrece a José Luis Barraza ser la tercera “pata” de la sociedad, quedando Drago, Navone y Barraza con el programa “Música de Motores” que se emitía por Radio Antártida.
<br />
<br />Más que periodismo deportivo, puso al servicio de sus convocantes su experiencia comercial generando la alternativa de continuar la campaña de Reutemann que se hizo a través de la emisora.
<br />
<br />En ese momento era más atractivo y económicamente rentable transmitir una carrera de Fórmula 1 para las organizaciones periodísticas incluyendo los medios gráficos. Con un teléfono de línea se hacía la transmisión. No era lo mismo transmitir en Tandil o Bahía Blanca con el frío a la intemperie tirando metros y metros de cables que estar sentado en un hotel y desde una ventana aledaña al circuito transmitiendo la carrera de Fórmula 1. Muchos medios locales dejaron de lado el automovilismo nacional fundamentalmente al TC, inclusive se empezaron a generar situaciones enojosas (en esos momentos estaban divididas las aguas entre el Automóvil Club Argentino y la ACTC) y todos se solidarizaban con el ACA en su mayoría, inclusive algunos medios en forma mentirosa mostraban fotografías donde decían “AQUÍ NO SE PUEDE CORRER EL TC”. La mayoría le daba la espalda al automovilismo autóctono quedando un vacío informativo y quiso el destino que fuese JLB partícipe de un acto de justicia. Allí lo puso la casualidad, Dios o el destino.
<br />
<br />Fue un poco insólito ese arranque, Barraza regresaba de un viaje de los Estados Unidos y a la semana siguiente se encuentra con un periodista de radio, el enorme Juan José Lujambio, al cual le comenta que de automovilismo no sabe nada y que un amigo lo convocó para hacer una revista de esa disciplina.
<br />
<br />Como estaba por finalizar el campeonato de Fórmula 1 y Reutemann estaba en condiciones de ser campeón, quien lo convoca a Lujambio le dice que la revista a publicar tenía prevista 2 tapas: si Reutemann salía campeón se llamaría “El Show de Carlos Reutemann”; si Reuteman no salía campeón se llamaría “El show de la Fórmula 1”.
<br />
<br />El caso es que necesitaban material fotográfico y periodístico, y como José Luis Barraza concurría a la última carrera de Fórmula 1 en Las Vegas donde se podía definir el campeonato entre Reutemann y Piquet, comenta que nunca había hecho nada gráfico, por lo que manifiesta que podía llevar una cámara de fotos y traer todo el material que los equipos de Fórmula 1 entregan y que es muy abundante. Él lo enviaría y en base a eso comenzaría el armado de la revista.
<br />
<br />Como él quedaba unos días en Las Vegas juntó todo el material y lo mandó por los colegas de Campeones (que volvían antes). Reutemann pierde el campeonato y la revista toma el nombre de “El Show de la Fórmula 1”.
<br />
<br />Barraza luego de disfrutar ese espectacular mundo que es Las Vegas regresa unos días después y los armadores de la revista piden hablar con él comentándole que tienen un grave problema: el editor se fue a Perú y se casó con una mujer peruana, por lo que los armadores se habían quedado sin trabajo. Entonces le preguntan si se puede hacer algo con el automovilismo. La respuesta fue afirmativa, y que algo se podía encarar y resucitar al TC por lo menos en la parte gráfica.
<br />
<br />Allí comienzan a darle forma al proyecto. Con Barraza eran 3 los que encararían este tema. El costo de la publicación se prorratearía entre los tres en partes iguales. Cada uno debía conseguir publicidad que sirva para palear los gastos. Este sería el nacimiento comercial de lo que después vendría con la publicación “TC… La Revista”.</p>

