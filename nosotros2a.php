      <p style="font-family:Oswald; font-size:17px; font-weight:lighter ">Cuando sale la edición número 1, se dan cuenta que la revista estaba periodísticamente hecha por Barraza, la totalidad de avisadores correspondían a su gestión, y que el trabajo de ellos era simplemente la diagramación. Barraza los reúne y les comenta que así la cosa no progresaría. Por ello ellos se retiran del proyecto y le dan vía libre para que él se haga cargo en su totalidad de la publicación a partir del número 2.<br />
<br />
Ese primer número de TC… La Revista salió en Junio de 1982 y era dedicado a la historia del Turismo Carretera a partir de su primera carrera en el año 1937. Fue presentada en la carrera de Concordia donde se corrió por primera y única vez. En la tapa los dos Aventín con los dos Dodge, el accidente de Juan Gálvez, la foto de Barraza transmitiendo la Fórmula 1 y el título principal de la tapa decía “De Causolett a los Aventín”. Así comenzó TC… La Revista.<br />
          <br />
        Los números se fueron generando espaciadamente, la situación económica no daba para más. Barraza iba a los distintos lugares donde corría el TC para vender la revista buscando a los canillitas del lugar para darle un paquete de las mismas y en los accesos de entradas así se vendían con mucho esfuerzo.<br />
          <br />
        En el inicio fue acompañado por sus hijos del primer matrimonio, Alejandra, como correctora poniendo en juego su profesión de educadora, Luis y Rodolfo debutando en el “metie” de vender publicidad junto a su padre. Pero allí no paraban las tareas. Desde preparar los envíos a los distintos distribuidores del interior y no haciendo asco a tomar una escoba o servir mate o café.<br />
          <br />
        Pero la cosa no era nada fácil. Traspies no faltaron. En una presentación en Bolívar y otra en 25 de Mayo debieron concurrir Barraza y su gente en 3 oportunidades ya que la lluvia impedía la realización de las competencias. Todo eso generaba pérdidas y desventuras pese a lo cual JLB no bajaba los brazos. Con el correr del tiempo comenzó a organizar la distribución en subtes, trenes y avenidas principales: todo con gente propia. Y TC… La Revista se mezclaba en los kioscos con demás publicaciones, lo que generó que un día la distribuidora general de diarios y revistas de la Capital Federa y Gran Buenos Aires le ofrece que se integre al sistema de recorrido por ellos organizado. Barraza y su gente tardaban 30 días en distribuir y otros 30 días en cobrar el dinero, en cambio, con esta distribuidora en 24 horas tenía todas las revistas los kioscos. Obviamente Barraza si bien perdía el 45% del valor de la revista vendida, la rápida evolución de ese dinero lo motivó a aceptar a integrarse a la distribuidora de Capital Federal y Gran Buenos Aires.<br />
          <br />
        Hubo distintas etapas: la primera por falta de experiencia, pero nos sentíamos satisfechos de iniciar la apertura en la recuperación de nuestro automovilismo que con el tiempo se concretaría y de la que nos sentíamos partícipes. Fueron 269 números que cumplieron un rol en la gráfica y de la cual hoy muchos colegas supieron tomar el testimonio y, con medios más modernos, mantienen vigente el estilo que nosotros inauguramos.<br />
          <br />
        Esta es una historia de esfuerzo y lucha que a veces termina con el amargo final de un fracaso y otros con el sabor de cumplimentar objetivos. Existen quienes alcanzan a comprender que el ser humano es un manojo de pasiones contenidas, pero sin duda José Luis Barraza logró hacer ese camino por casualidad, necesidad y vocación.        </p>
