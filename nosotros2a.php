        Teníamos en nuestra humilde redacción gran cantidad de cartas de lectores. Las temáticas eran varias y algunas hasta insólitas. Críticas, felicitaciones, propuestas, pedidos, etcétera.<br />
              <br />
        Entre ellas llegó una carta que era firmada por una tal “Mabel”. En ella decía haber sido compañera de labores en la empresa electrónica. Haciendo memoria recordé que mientras yo en ella era jefe de expedición, había una Mabel que era secretaria del presidente de la empresa y de la que poco trato tuvimos, sobre todo por su cargo y la diferencia de pisos del establecimiento con más de 120 personas en derredor entre hombres y mujeres.<br />
              <br />
        Esa carta, que en principio sorprendió a Luis Santiago (mi hijo) y obviamente a mí, quedó archivada en un cajón.<br />
              <br />
        El trabajo nos desbordaba ya que TC… La Revista cada día ingresaba a nuevos pueblos y ciudades que la descubrían con una temática que otros medios había desechado. El TC tenía distintos detractores, parte del periodismo era uno de ellos.<br />
              <br />
        Con el paso de algo más de un mes y por sugerencia de mi hijo Luis Santiago decidimos releer la carta que nos había mandado esa tal “Mabel”. En la carta decía: “… Soy Mabel Giaconi, no se si se acuerdan de mi. Yo era secretaria del ingeniero Ennio Pogliano presidente de la empresa donde trabajaba yo y JLB. Siempre seguí el automovilismo desde pequeña con mis padres. Carpas y alambrados fueron el entorno de cada carrera que íbamos, pero en el medio donde yo trabajaba ser “fierrera” no era bien visto. Yo compro su revista que me hace sentir muy cerca del automovilismo. Hoy trabajo en una empresa de ultramar y tengo horario de tarde y mucho me gustaría, aunque sea unas pocas horas, estar con ustedes siendo auxiliar: preparar café, ordenar cosas, barrer, etcétera. Sería una forma de seguir estando cerca del automovilismo y recuperar el espacio que mis obligaciones laborales y las distancias me impiden compartir. Ustedes sería el nexo que me permitiría estar actualizada y sentirme parte de lo que tanto extraño…”<br />
              <br />
        Y llamamos a Mabel y debimos en algún caso sacarle la escoba de las manos queriendo cumplimentar cualquier tarea auxiliar que se presente. Ella, en esa instancia como lo informó en la carta, trabajaba en una empresa de Ultramar en la calle Bartolomé Mitre casi Maipú. Su horario era de 18 a 23 hs. por esa razón pasaba con nosotros hasta media tarde. Su labor en esa empresa era manejar máquinas de teletipo con su excelente conocimiento de inglés y francés.<br />
              <br />
        Sin ninguna duda el ingreso de Mabel cambió la instancia laboral pero también cambió mi vida.
